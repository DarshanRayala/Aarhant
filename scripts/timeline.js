
document.getElementById("firCon").onclick = function () {
    // const child = document.getElementById("secCon")
    const child = document.querySelectorAll("#secCon")
    const child2 = document.querySelectorAll("#secCon2")
    const child3 = document.querySelectorAll("#secCon3")
    const child4 = document.querySelectorAll("#secCon4")
    const child5 = document.querySelectorAll("#secCon5")
    console.log(child);
    for (var i = 0; i < child.length; i++) {
        child[i].classList.toggle("toggleDisplay")
        child2[i].classList.add("toggleDisplay")
        child3[i].classList.add("toggleDisplay")
        child4[i].classList.add("toggleDisplay")
        child5[i].classList.add("toggleDisplay")
    }
}
